/**
 * Realizar un ejercicio que nos muestre la tabla de multiplicar del 6
 * 
 * @author TiRii
 *
 */

public class Ejercicio1 {

	public static void main(String[] args) {
		
		
		int numero=6;
		
		for(int i=1;i<=numero;i++) {
			
			System.out.println(i + "x"+ numero + "=" + i*numero);
			
		}

	}

}
