package ejercicios_listas;

public class Jugador {

	// Atributos clase
	private String nombre;
	private int dorsal;

	// Constructor
	public Jugador() {

	}

	// Getters y Setters
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getDorsal() {
		return dorsal;
	}

	public void setDorsal(int dorsal) {
		this.dorsal = dorsal;
	}

}
